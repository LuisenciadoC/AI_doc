class Usuario:
    def __init__(self, id_usuario, nombre, correo, contraseña, estado, fecha_creacion, id_rol):
        self.id_usuario = id_usuario
        self.nombre = nombre
        self.correo = correo
        self.contraseña = contraseña
        self.estado = estado
        self.fecha_creacion = fecha_creacion
        self.id_rol = id_rol